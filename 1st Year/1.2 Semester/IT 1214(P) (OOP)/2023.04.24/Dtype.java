class  Dtype
{
	public static void main(String args[])
	{
		boolean flag = true;
		System.out.println(flag);  //print true
		
		byte range;
		range = 124;
		System.out.println(range); //print 124
		
		short temprature;
		temprature = -200;
		System.out.println(temprature); //print -200
		
		int range1 = -4250000;
		System.out.println(range1); //print -4250000
		
		float number1 = -42.5f;
		System.out.println(number1); //print -42.50
		
		char letter = '\u0051'; //(/u mean unicode)
		System.out.println(letter); //print Q
		
		char letter1 = '9';
		System.out.println(letter1); //print 9
		
		char letter2 = 65;
		System.out.println(letter2); //print A
	}
}