//looping through array element
class Array2
{
	public static void main(String args[])
	{
		//create new array
		int[] age = {12,4,5};
		
		//loop through array using for loop
		System.out.println("using for loop: ");
		for (int i=0; i<age.length; i++)
		{
			System.out.println(age[i]);
			
		}
	}
}